def parse_llm_evaluation(llm_output: str) -> dict:
    """
    Parses the LLM's evaluation output using regular expressions.

    Args:
        llm_output: The complete string generated by the language model.

    Returns:
        A dictionary containing the critic, conclusion, and final choice.
        Values will be None if the corresponding part is not found.
    """

    def extract_text(input_pattern):
        matches = re.findall(input_pattern, llm_output, re.DOTALL)
        if not matches:
            return None
        return matches[-1].strip()

    think_pattern = r"<think>(.*?)</think>"
    critic_pattern = r"<critics>(.*?)</critics>"
    fallback_critic_pattern = r"<strengths_A>(.*?)</weaknesses_B>"
    strengths_A_pattern = r"<strengths_A>(.*?)</strengths_A>"
    weaknesses_A_pattern = r"<weaknesses_A>(.*?)</weaknesses_A>"
    strengths_B_pattern = r"<strengths_B>(.*?)</strengths_B>"
    weaknesses_B_pattern = r"<weaknesses_B>(.*?)</weaknesses_B>"
    conclusion_pattern = r"<conclusion>(.*?)</conclusion>"
    choice_pattern = r"<choice>(.*?)</choice>"
    fallback_choice_pattern = r"\[\[\s*(A|B)\s*\]\]"

    critics = extract_text(critic_pattern)

    choice = extract_text(choice_pattern)
    if choice is None:
        fallback_match = re.search(fallback_choice_pattern, llm_output)
        if fallback_match:
            choice = fallback_match.group(1)
    if choice is not None:
        if "A" in choice and "B" not in choice:
            choice = "A"
        elif "B" in choice and "A" not in choice:
            choice = "B"
        else:
            choice = None

    return {
        "think": extract_text(think_pattern),
        "critics": critics,
        "strengths_A": extract_text(strengths_A_pattern),
        "weaknesses_A": extract_text(weaknesses_A_pattern),
        "strengths_B": extract_text(strengths_B_pattern),
        "weaknesses_B": extract_text(weaknesses_B_pattern),
        "conclusion": extract_text(conclusion_pattern),
        "choice": choice,
    }
